export const getAdviceItems = (weather) => {
  const items = [];

  if (weather.humidity >= 75) {
    items.push({
      title: 'Что взять с собой',
      text: 'Возьмите зонт или легкую непромокаемую куртку: воздух влажный, возможны осадки.',
    });
  } else if (weather.temperature >= 25) {
    items.push({
      title: 'Что взять с собой',
      text: 'Вода, очки и головной убор будут кстати, потому что день теплый.',
    });
  } else {
    items.push({
      title: 'Что взять с собой',
      text: 'Подойдет обычная повседневная одежда по сезону без дополнительных вещей.',
    });
  }

  if (weather.windSpeed >= 25) {
    items.push({
      title: 'Как провести день',
      text: 'Лучше выбирать спокойные дела и учитывать сильный ветер при прогулке.',
    });
  } else if (weather.temperature >= 20) {
    items.push({
      title: 'Как провести день',
      text: 'Погода подходит для прогулки, короткой поездки или встречи на свежем воздухе.',
    });
  } else {
    items.push({
      title: 'Как провести день',
      text: 'Если не хочется долго быть на улице, можно выбрать спокойные дела в помещении.',
    });
  }

  return items;
};
